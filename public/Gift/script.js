const cardsFilterLinks = document.getElementById("gift-card-filters");
const giftCardsSection = document.querySelector(".gift-cards")
cardsFilterLinks.addEventListener("click",(e)=>{
  e.stopPropagation()
  if (!e.target.classList.contains("cards-link")) {
    // Check if the clicked element is not a child with class "cards-link"
    return; // If not, do nothing
  }
  document.querySelectorAll(".cards-link").forEach((link)=>{
    link.classList.remove("active") 
  })
  const cardsId = e.target.id  
  giftCardsSection.innerHTML =""
  getGiftsFilters(cardsId)
})
const getGiftsFilters = (id) => {
  const linkName = document.getElementById(id);
  linkName.classList.toggle("active")
    giftCards.filter((value)=>
        value.id === id).map((el)=>{
          giftCardsSection.innerHTML +=`<div class="filtered-cards">
               <div class="image-card">
                <img src="${el.img}" width="200"/>
               </div>
               <div class="description">
               <h2>${el.name}</h2>
               <p>${el.description}</p>
               <button class="add-item-btn" id="${el.name.replace(/[\s]/g,"-")}">Add Item</button>
               </div>
            </div>`
        })
        }
